#include "ft_printf.h"

int	ft_atoi(const char *s, int a_neg, int *rt, size_t *i)
{
	long	n;
	int		np;

	np = (s[*i] == '-') ? -1 : 1;
	if (s[*i] == '-' || s[*i] == '+')
	{
		if (a_neg)
			(*i)++;
		else
			return (1);
	}
	n = 0;
	while (s[*i] >= '0' && s[*i] <= '9')
	{
		n *= 10;
		n += s[*i] - 48;
		(*i)++;
	}
	*rt = ((int)n * np);
	return (0);
}
